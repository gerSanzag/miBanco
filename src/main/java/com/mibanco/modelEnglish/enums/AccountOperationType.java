package com.mibanco.modelEnglish.enums;

/**
 * Types of operations that can be performed with accounts
 */
public enum AccountOperationType {
    CREATE,         // Crear
    MODIFY,         // Modificar
    DELETE,         // Eliminar
    ACTIVATE,       // Activar
    DEACTIVATE,     // Desactivar
    BLOCK,          // Bloquear
    UNBLOCK,        // Desbloquear
    UPDATE,         // Actualizar
    RESTORE         // Restaurar
} 